                History of Ghaziabad
              </button>
            </h2>
            <div id="collapseTwo" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
              <div class="accordion-body">
                <strong>The studies and excavations made on the Kaseri mound at the banks of river Hindon </strong>prove
                that the history of Ghaziabad dates back to 2500 BC. The myth also suggests the existence of the city in
                the times of Mahabharat. The fort at Loni has been associated with the Ramayan. The city was founded in
                AD 1740 by the Wazir (minister) Ghazi-ud-din of Mughal empire, who named the city after himself-
                Ghaziuddin Nagar. Later it was shortened and came to be known as Ghaziabad.

                Situated on the banks of river Hindon, the town served as the picnic spot for the royal family for a
                long time. Meanwhile, it was ruled by several emperors including Mohammad-bin-Tughlaq, Taimur, Mughals,
                Marathas etc. The village of Kot is famously linked with the reign of king Samudragupta as he performed
                several Ashvamedh Yagnas.

                Ghaziabad was also associated with the Indian independence movement from the British rebellion of
                1857-58. This war is also called the first war of independence in the nation. Until 1976, Ghaziabad was
                a tehsil in the district of Meerut. Following which, it became an independent district, and it has been
                growing ever since, by leaps and bounds.
