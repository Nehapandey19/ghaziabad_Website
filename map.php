                hotels. In addition to local street food and main courses from both North and South India, Ghaziabad
                also serves global food composing of Italian and Chinese cuisines.

                This part of Uttar Pradesh also carries out its own flavours of Gujarati food such as Phaphda and
                Dhokla. The typical North Indian platter consisting of roti, rice, vegetables and dal (lentils curry) is
                the city's larger staple. Besides, Ghaziabad also has its own array of street food or chaat including
                sweet corn chaat, Chole Bhatura.
